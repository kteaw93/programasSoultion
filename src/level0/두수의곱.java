package level0;

public class 두수의곱 {
 
	public static void main(String[] args) {
		int num1 = 3; 
		int num2 = 7;  
		
		int result = solution(num1,num2);
		
	}

//정수 num1, num2가 매개변수 주어집니다. num1과 num2를 곱한 값을 return 하도록 solution 함수를 완성해주세요.
	private static int solution(int num1, int num2) {
		
		return num1 * num2;
	}
}
